/*
No son mas que contenedores que almacenan datos

Las variables se declaran usando las palabras reservadas

var: Es la forma tradicional pero ya no es recomendada
let: Es la forma moderna, es la recomendada
const: El dato almacenado no puede cambiar

Para declarar una variable:

var nambre variable: valor;

*/

nombre = "Alejandro";
edad = 20;
vivo = true;

console.log(nombre);
console.log(edad);
console.log(vivo);


/*
Las variables declaradas con "var" y "let" pueden cambiar a lo largo del codigo
siempre y cuando su alcance lo permite

Las variables declaradas con "const" no pueden cambiar.
Si lo intentamos marcara un error
*/

nombre = "Puchungito"

console.log(nombre);
